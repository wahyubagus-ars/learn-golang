package main

import "fmt"

func main() {
	var name string

	name = "Bruce Wayne"
	fmt.Println(name)

	name = "Batman"
	fmt.Println(name)

	var friendName = "Budi"
	fmt.Println(friendName)

	var age = 30
	fmt.Println(age)

	lastName := "Utomo"
	fmt.Println(lastName)

	//multipe variable

	var (
		firstName = "Clark"
		lastName2 = "Kent"
	)

	fmt.Println(firstName)
	fmt.Println(lastName2)
}
